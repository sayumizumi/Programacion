import java.util.*;

public class contadores {

    public static void main(String[] args) {

        // -----variables
Scanner sc = new Scanner(System.in);

        for (int i = 0; i < 101; i++) {
            System.out.println(i);
        }
        System.out.println("---------------");
        sc.next();

        for (int i = 0; i > -101; i--) {
            System.out.println(i);
        }
    }
}